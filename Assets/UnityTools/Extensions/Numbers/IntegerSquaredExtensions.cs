namespace UnityTools.Extensions
{
    public static class IntegerSquaredExtensions
    {
        public static int Squared(this sbyte n)
        {
            return n * n;
        }

        public static int Squared(this byte n)
        {
            return n * n;
        }

        public static int Squared(this short n)
        {
            return n * n;
        }

        public static int Squared(this ushort n)
        {
            return n * n;
        }

        public static int Squared(this int n)
        {
            return n * n;
        }

        public static uint Squared(this uint n)
        {
            return n * n;
        }

        public static long Squared(this long n)
        {
            return n * n;
        }

        public static ulong Squared(this ulong n)
        {
            return n * n;
        }
    }
}